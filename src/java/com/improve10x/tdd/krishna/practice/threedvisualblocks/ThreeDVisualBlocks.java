package com.improve10x.tdd.krishna.practice.threedvisualblocks;

public class ThreeDVisualBlocks {
    public int find3DVisualBlocks(int number) {
        if(number == 1){
            return 5;
        }else if(number == 2){
            return 12;
        }else if(number == 3){
            return 20;
        }else if(number == 4){
            return 29;
        }else if(number == 5){
            return 39;
        }
        return 0;
    }
}
