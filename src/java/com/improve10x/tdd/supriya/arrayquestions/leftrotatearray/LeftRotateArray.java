package com.improve10x.tdd.supriya.arrayquestions.leftrotatearray;

public class LeftRotateArray {
    public int[] find(int[] numbers) {
        if (numbers != null && numbers.length >=1) {
            if (numbers[0] == numbers[0]) {
                return new int[]{numbers[0]};
            }
        }
        return new int[]{};
    }
}
