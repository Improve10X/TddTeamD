package com.improve10x.tdd.team.scoringone;

import com.improve10x.tdd.team.scoringtest.ScoringSystem;
import org.junit.jupiter.api.Assertions;
import org.junit.jupiter.api.Test;

import static org.junit.jupiter.api.Assertions.assertArrayEquals;
import static org.junit.jupiter.api.Assertions.assertEquals;

//"A"->1,0,0
//"ABC"-> 1,1,1
//"ABCBACC"->
//ABBACCCCAC->
public class ScoringSystemTest {

    @Test
    public void nothing(){

    }
    @Test
    public void givensA_returnsOneZerZero(){
        ScoringSystem scoringSystem = new ScoringSystem();
        int[] onlyAScore = scoringSystem.calculates("A");
        assertArrayEquals(new int[]{1,0,0},onlyAScore );
    }
    @Test
    public void givensABC_returnsOneOneOne(){
        ScoringSystem scoringSystem = new ScoringSystem();
        int[] Score = scoringSystem.calculates("ABC");
        assertArrayEquals(new int[]{1,1,1},Score);
    }
    @Test
    public void givensABBACCCCAC_returnsOneOneOne(){
        ScoringSystem scoringSystem = new ScoringSystem();
        int[] Score = scoringSystem.calculates("ABBCCCCAC");
        assertArrayEquals(new int[]{2,2,5},Score);
    }

}
